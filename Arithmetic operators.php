<?php
$j = 10;  // starting value

// Addition
echo "Addition: " . ($j + 1) . "\n";   // 10 + 1 = 11

// Subtraction
echo "Subtraction: " . ($j - 6) . "\n";  // 10 - 6 = 4

// Multiplication
echo "Multiplication: " . ($j * 11) . "\n"; // 10 * 11 = 110

// Division
echo "Division: " . ($j / 4) . "\n";  // 10 / 4 = 2.5

// Modulus (remainder)
echo "Modulus: " . ($j % 9) . "\n";   // 10 % 9 = 1

// Increment (increase by 1)
echo "Increment: " . (++$j) . "\n";   // now $j = 11

// Decrement (decrease by 1)
echo "Decrement: " . (--$j) . "\n";   // now $j = 10 again

// Exponentiation (power)
echo "Exponentiation: " . ($j ** 2) . "\n";  // 10^2 = 100
?>
